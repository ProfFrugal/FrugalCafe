        static HashHelpers()
        {
            Primes24 = AddNoLohPrimes(24);
            Primes16 = AddNoLohPrimes(16, 11);
        }

        public static int[] AddNoLohPrimes(int elementSize, params int[] remove)
        {
            HashSet<int> combined = new HashSet<int>(HashHelpers.OldPrimes);

            foreach(var p in remove)
            {
                combined.Remove(p);
            }

            foreach (var p in HashHelpers.NoLohSequence(elementSize))
            {
                combined.Add(p);
            }

            var results = new int[combined.Count];

            combined.CopyTo(results, 0);

            Array.Sort(results);

            return results;
        }

        public static IEnumerable<int> NoLohSequence(int elementSize)
        {
            int candidate = (85000 - 24) / elementSize;

            while (candidate >= 2)
            {
                if (candidate != 2)
                {
                    if ((candidate & 1) == 0)
                    {
                        candidate--;
                    }

                    while (!HashHelpers.IsPrime(candidate))
                    {
                        candidate -= 2;
                    }
                }

                yield return candidate;

                candidate /= 2;
            }
        }

        public static int Combine(this int h1, int h2)
        {
            uint num = (uint)(h1 << 5) | ((uint)h1 >> 27);
            return ((int)num + h1) ^ h2;
        }

        public static int GetPrime(int min)
        {
            return GetPrime(min, OldPrimes);
        }

        public static int GetPrimeAll(int min)
        {
            if (min <= 2)
            {
                return 2;
            }

            int slot = (min - 3) / 2;

            if (slot < NextPrimes.Length)
            {
                return NextPrimes[slot];
            }

            return GetPrime(min);
        }

        public static int GetPrime(int min, int[] primes)
        {
            if (min < 0)
            {
                throw new ArgumentOutOfRangeException(nameof(min));
            }

            for (int i = 0; i < primes.Length; i++)
            {
                int num = primes[i];
                
                if (num >= min)
                {
                    return num;
                }
            }

            for (int j = min | 1; j < int.MaxValue; j += 2)
            {
                if (IsPrime(j))
                {
                    return j;
                }
            }
            
            return min;
        }

        public static bool IsPrime(int candidate)
        {
            if ((candidate & 1) != 0)
            {
                for (int divisor = 3; (divisor * divisor) <= candidate; divisor += 2)
                {
                    if ((candidate % divisor) == 0)
                    {
                        return false;
                    }
                }

                return true;
            }

            return (candidate == 2);
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static ulong GetFastModMultiplier(uint divisor)
        {
            return ulong.MaxValue / (ulong)divisor + 1;
        }

        [MethodImpl(MethodImplOptions.AggressiveInlining)]
        public static uint FastMod(uint value, uint divisor, ulong multiplier)
        {
            return (uint)(((multiplier * value >> 32) + 1) * divisor >> 32);
        }
